import UIKit

extension UIColor {
    class func greenNice() -> UIColor {
        return UIColor(red: 32/255, green: 188/255, blue: 32/255, alpha: 1)
    }
    class func blueDark() -> UIColor {
        return UIColor(red: 14/255, green: 88/255, blue: 149/255, alpha: 1)
    }
    class func blueBaby() -> UIColor {
        return UIColor(red: 15/255, green: 193/255, blue: 231/255, alpha: 1)
    }
    class func blueDefault() -> UIColor {
        return UIColor(red: 0/255, green: 122/255, blue: 255/255, alpha: 1)
    }
    class func ViewBackground() -> UIColor {
        return UIColor(red: 230/255, green: 230/255, blue: 230/255, alpha: 1)
    }
    class func NavigationBarTint() -> UIColor {
        return UIColor(red: 130/255, green: 130/255, blue: 130/255, alpha: 1)
    }
    class func NavigationBarBackground() -> UIColor {
        return UIColor(red: 50/255, green: 50/255, blue: 50/255, alpha: 1)
    }
    class func NavigationBarSearchBackground() -> UIColor {
        return UIColor(red: 30/255, green: 30/255, blue: 30/255, alpha: 1)
    }
}
